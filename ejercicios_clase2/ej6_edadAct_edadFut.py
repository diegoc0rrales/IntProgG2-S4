ano_nacimiento = int(input("Ingresa tu año de nacimiento: "))
ano_actual = int(input("Ingresa el año actual: "))

edad_actual = ano_actual - ano_nacimiento

edad_10_anos = edad_actual + 10

print(f"Tu edad actual es: {edad_actual} años.")
print(f"Tu edad dentro de 10 años será: {edad_10_anos} años.")
